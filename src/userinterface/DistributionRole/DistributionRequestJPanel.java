    private boolean namePatternCorrect(){
        Pattern p = Pattern.compile("^[a-zA-Z]{6,15}+$");
        Matcher m=p.matcher(prodNameJTextField.getText());
        boolean b = m.matches();
        return b;
    }
    
    private boolean quantityPatternCorrect(){
        Pattern p = Pattern.compile("^[0-9]+$");
        Matcher m=p.matcher(quantityJTextField.getText());
        boolean b = m.matches();
        return b;
    }
    
    private boolean notePatternCorrect(){
        Pattern p = Pattern.compile("^[a-zA-Z]{6,30}+$");
        Matcher m=p.matcher(noteJTextField.getText());
        boolean b = m.matches();
        return b;
    }

