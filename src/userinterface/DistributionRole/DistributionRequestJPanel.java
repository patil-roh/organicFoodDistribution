    private void requestTestJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_requestTestJButtonActionPerformed

        if(!namePatternCorrect()){
           JOptionPane.showMessageDialog(null, "Please enter a valid product name");
           prodNameJTextField.setBorder(BorderFactory.createLineBorder(Color.RED));
           jLabel1.setForeground(Color.RED);
           return;
       }
        if(!quantityPatternCorrect()){
           JOptionPane.showMessageDialog(null, "Please enter a valid input");
           prodNameJTextField.setBorder(BorderFactory.createLineBorder(Color.RED));
           jLabel2.setForeground(Color.RED);
           return;
       }
        if(!notePatternCorrect()){
           JOptionPane.showMessageDialog(null, "Please enter a valid input");
           prodNameJTextField.setBorder(BorderFactory.createLineBorder(Color.RED));
           jLabel3.setForeground(Color.RED);
           return;
       }
        
        WarehouseWorkRequest request = new WarehouseWorkRequest();
        request.setProductName(prodNameJTextField.getText());
        request.setQuantity(Integer.parseInt(quantityJTextField.getText()));
        request.setAddNote(noteJTextField.getText());

        Organization org = null;
        for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()){
            if (organization instanceof WarehouseOrganization){
                org = organization;
                break;
            }
        }
        if (org!=null){
            org.getWorkQueue().getworkRequestOrderList().add(request);
            userAccount.getWorkQueue().getworkRequestOrderList().add(request);
        }
    }//GEN-LAST:event_requestTestJButtonActionPerformed

